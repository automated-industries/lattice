/**
 * Column + table definitions — one source of truth for "what does this mean?".
 *
 * A definition is consumed in three places:
 *   1. the GUI hover tooltip on table headers / field labels / entity cards,
 *   2. the assistant's schema context (so a good description improves the
 *      model's categorization + extraction), and
 *   3. the `set_definition` assistant tool (write-back).
 *
 * Resolution order: an operator-authored value (stored in
 * `_lattice_gui_column_meta.description` / `_lattice_gui_meta.description`) wins;
 * otherwise a built-in default (native entities + common system columns);
 * otherwise none (the caller falls back to the column's type/role).
 *
 * Definitions for user-created columns/tables are auto-generated by a cheap,
 * non-blocking, fail-silent Haiku pass at creation time (see the generators
 * below) — never overwriting an authored value.
 */
import type { Lattice } from '../lattice.js';
// Type-only import (erased at runtime) so this module stays a leaf — importing
// the runtime `DEFAULT_MODEL` from ai/chat would form a cycle once ai/dispatch
// imports this module for set_definition. The model const is inlined instead.
import type { LlmClient } from './ai/chat.js';
import { isNativeEntity } from '../framework/native-entities.js';

/** Cheapest capable model for the throwaway definition-generation passes. */
const GEN_MODEL = 'claude-haiku-4-5';

